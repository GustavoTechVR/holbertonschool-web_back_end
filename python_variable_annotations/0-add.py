#!/usr/bin/env python3
"""
Module with a type-annotated function add
"""

def add(a: float, b: float) -> float:
    """
    Function that takes two floats and returns their sum.

    Args:
        a (float): The first input float.
        b (float): The second input float.

    Returns:
        float: The sum of the input floats.
    """
    return a + b
